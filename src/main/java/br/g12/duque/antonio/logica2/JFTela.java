    private void btnLongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLongActionPerformed
        // TODO add your handling code here:
        long valor1 = Long.valueOf(txtValor1.getText());
        long valor2 = Long.valueOf(txtValor2.getText());
        long resultado = valor1 * valor2 - (valor2 + valor1);
        txtResultado.setText(String.valueOf(resultado));
    }//GEN-LAST:event_btnLongActionPerformed

    private void btnFloatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFloatActionPerformed
        // TODO add your handling code here:
        float preco = Float.parseFloat(txtValor1.getText());
        float quantidade = Float.parseFloat(txtValor2.getText());
        float total = preco * quantidade;
        String strTotal = String.valueOf(total);
        strTotal = strTotal.replace(".", ",");
        txtResultado.setText("R$ "+ strTotal);
    }//GEN-LAST:event_btnFloatActionPerformed

    private void btnDoubleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoubleActionPerformed
        // TODO add your handling code here:
        final double DOLAR = 4.98;
        double valor = Double.valueOf(txtValor1.getText());
        txtValor2.setText("R$ "+ String.valueOf(DOLAR).replace(".", ","));
        double total = valor/DOLAR;
        DecimalFormat df = new DecimalFormat("US$ ,##0.00");
        String strTotal = df.format(total);
        txtResultado.setText(strTotal);
    }//GEN-LAST:event_btnDoubleActionPerformed

    private void btnCambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambioActionPerformed
         // TODO add your handling code here:
        final double DOLAR = 4.98;
        final double EURO = 5.41;
        double valor = Double.parseDouble(txtValor1.getText());
        double total = 0;
        
        if(rbDolar.isSelected()){
            total = valor/DOLAR;
            txtValor2.setText(String.valueOf(DOLAR));
        }
        
        if(rbEuro.isSelected()){
            total = valor/EURO;
             txtValor2.setText(String.valueOf(EURO));
        }
        
        if(rbReal.isSelected()){
             txtValor2.setText("R$ 1,00");
        }
        
        
    }//GEN-LAST:event_btnCambioActionPerformed

